/**
 * 获取汉字的拼音
 * @param char 单个汉字
 * @returns 拼音字符串，如果没有映射则返回空字符串
 */
export function getPinyin(char: string): string {
  return PINYIN_MAP[char] || '';
}

/**
 * 检查搜索词是否匹配拼音
 * @param tag 标签文本
 * @param query 搜索词
 * @returns 是否匹配
 */
export function matchesPinyin(tag: string, query: string): boolean {
  // 直接包含
  if (tag.toLowerCase().includes(query.toLowerCase())) return true;

  // 拼音匹配
  let pinyinStr = '';
  for (const char of tag) {
    const py = getPinyin(char);
    if (py) {
      pinyinStr += py;
    } else {
      pinyinStr += char;
    }
  }

  return pinyinStr.toLowerCase().includes(query.toLowerCase());
}
